// Key path type for type-safe t(key)
export type TranslationKey = keyof typeof translations.en
export type NavKey = keyof typeof translations.en.nav
export type HeroKey = keyof typeof translations.en.hero
export type FooterKey = keyof typeof translations.en.footer
export type LangKey = keyof typeof translations.en.lang

export type TranslationSectionKey = keyof typeof translations.en

export function getTranslation(locale: Locale, section: TranslationSectionKey, key: string): string {
  const sectionObj = translations[locale][section] as Record<string, string> | undefined
  const fallback = (translations.en[section] as Record<string, string> | undefined)?.[key]
  return sectionObj?.[key] ?? fallback ?? key
}
